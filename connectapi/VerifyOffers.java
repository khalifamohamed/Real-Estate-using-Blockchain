    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            String tenantEmail = jTextField1.getText();
            int n = (int) jSpinner1.getValue();
            
        if(this.jTextField1.getText().isEmpty()|| (int) this.jSpinner1.getValue() == 0){
        JOptionPane.showMessageDialog(null, "Some Fields are not filled.");}
        else {
            //http://localhost:8080/enlistments/{{enlistmentId}}/offers/review
            OfferReview offrev = new OfferReview(tenantEmail, true);
            String s = connectapi.APIManager.sendRequest("enlistments/" + n + "/offers/review",offrev.SendRequestToBlockchain() );
            JOptionPane.showMessageDialog(null, offrev.SendRequestToBlockchain());
            APIResponse apir = new APIResponse(s);
            apir.setVisible(true);
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

